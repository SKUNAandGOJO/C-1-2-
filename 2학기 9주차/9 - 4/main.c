#include <stdio.h>
#include <stdlib.h>
#include <limits.h>
#include <float.h>

int main(void) {
	int num = 0;
	while (1) {
		printf("정수 입력 : ");
		scanf("%d", &num);

		if (num == 4) {
			exit(0);
		}
		else if (num == 1){
			int i = 1;
			printf("                                                                         구구단                                                           \n");
			printf("==========================================================================================================================================\n");
			printf(" 1단             2단             3단             4단             5단             6단             7단             8단             9단      \n");
			printf("==========================================================================================================================================\n");
			while (i < 9 + 1) {
				for (int j = 1; j < 9 + 1; j++) {
					printf("%d X %d = %d\t", j, i, i * j);

				}
				printf("\n");
				i++;
			}
			printf("==========================================================================================================================================\n");
			system("pause");
			system("cls");

		}
		else if (num == 2) {
			int i = 9;
			printf("                                                                         구구단                                                           \n");
			printf("==========================================================================================================================================\n");
			printf(" 9단             8단             7단             6단             5단             4단             3단             2단             1단      \n");
			printf("==========================================================================================================================================\n");
			while (i > 1) {
				for (int j = 9; j > 0; j--) {
					printf("%d X %d = %d\t", j, i, i * j);

				}
				printf("\n");
				i--;
			}
			printf("==========================================================================================================================================\n");
			system("pause");
			system("cls");
		}
		else if (num == 3) {
			int i = 1;
			int a = 0;

			printf("정수 입력 : ");
			scanf("%d", &a);

			printf("                                                                         구구단                                                           \n");
			printf("==========================================================================================================================================\n");
			printf(" 1단             2단             3단             4단             5단             6단             7단             8단             9단      \n");
			printf("==========================================================================================================================================\n");
			while (i < 9 + 1) {
				for (int j = 1;  j < a+1; j++) {
					printf("%d X %d = %d\t", j, i, i * j);

				}
				printf("\n");
				i++;
			}
			printf("==========================================================================================================================================\n");
		
		}
		else {
			printf("다시 입력");
			system("pause");
			system("cls");

		}
	}

	return 0;
}