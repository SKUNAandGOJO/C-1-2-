#include <stdio.h>
#include <stdlib.h>
#include <limits.h>
#include <float.h>
#include <windows.h>

int main(void) {

	int i = 1;
	printf("                                                                         구구단                                                           \n");
	printf("==========================================================================================================================================\n");
	printf(" 1단             2단             3단             4단             5단             6단             7단             8단             9단      \n");
	printf("==========================================================================================================================================\n");
	while (i < 9 + 1) {
		for (int j = 1; j < 9 + 1; j++) {
			printf("%d X %d = %d\t", j, i, i * j);

		}
		printf("\n");
		i++;
	}
	printf("==========================================================================================================================================\n");
	return 0;
}